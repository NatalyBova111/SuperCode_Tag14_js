// Функция для округления числа
function roundNumber(value) {
return Math.round(value);
}

// Тестовые значения
const num1 = 4.3;
const num2 = 7.8;
const num3 = 1.5;

// Вывод результатов в консоль
console.log("Ergebnis 1:", roundNumber(num1)); // 4
console.log("Ergebnis 2:", roundNumber(num2)); // 8
console.log("Ergebnis 3:", roundNumber(num3)); // 2