// Случайное число от 0 (включительно) до 1 (не включая 1)
const randomNum = Math.random();
console.log("Zufallszahl (0–1):", randomNum);

// Случайное целое число от 1 до 10
const randomNum1_10 = Math.floor(Math.random() * 10) + 1;
console.log("Zufallszahl (1–10):", randomNum1_10);

// Случайное целое число от 1 до 100
const randomNum1_100 = Math.floor(Math.random() * 100) + 1;
console.log("Zufallszahl (1–100):", randomNum1_100);